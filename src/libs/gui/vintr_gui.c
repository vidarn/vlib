#include "gui/gui.h"
#include <stdlib.h>
int vintr_load_gui_font(struct GameData *data)
{
	return load_font("Montserrat-SemiBold", 30.f, data);
}

void vintr_set_gui_style(struct nk_context *ctx)
{
	struct nk_color *color_table = calloc(NK_COLOR_COUNT, sizeof(struct nk_color));

	#define SET_COLOR(i,r,g,b,a) color_table[i] = nk_rgba(r,g,b,a)

	SET_COLOR(NK_COLOR_TEXT, 255, 255, 255, 255);
	SET_COLOR(NK_COLOR_WINDOW, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_HEADER, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_BORDER, 0, 0, 0, 0);
	SET_COLOR(NK_COLOR_BUTTON, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_BUTTON_HOVER, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_BUTTON_ACTIVE, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_TOGGLE, 255, 255, 255, 255);
	SET_COLOR(NK_COLOR_TOGGLE_HOVER, 255, 255, 255, 255);
	SET_COLOR(NK_COLOR_TOGGLE_CURSOR, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_SELECT, 255, 255, 255, 255);
	SET_COLOR(NK_COLOR_SELECT_ACTIVE, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_SLIDER, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_SLIDER_CURSOR, 255, 255, 255, 255);
	SET_COLOR(NK_COLOR_SLIDER_CURSOR_HOVER, 255, 255, 255, 255);
	SET_COLOR(NK_COLOR_SLIDER_CURSOR_ACTIVE, 255, 255, 255, 255);
	SET_COLOR(NK_COLOR_PROPERTY, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_EDIT, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_EDIT_CURSOR, 255, 255, 255, 255);
	SET_COLOR(NK_COLOR_COMBO, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_CHART, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_CHART_COLOR, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_CHART_COLOR_HIGHLIGHT, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_SCROLLBAR, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_SCROLLBAR_CURSOR, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_SCROLLBAR_CURSOR_HOVER, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_SCROLLBAR_CURSOR_ACTIVE, 0, 0, 0, 255);
	SET_COLOR(NK_COLOR_TAB_HEADER, 0, 0, 0, 255);

	nk_style_from_table(ctx, color_table);
}